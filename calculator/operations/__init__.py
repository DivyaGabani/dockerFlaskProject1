""" These are the Operation Classes"""


class Addition:
    """ This is the addition class"""

    @staticmethod
    def add(num_1, num_2):
        """ This is the add method"""
        return num_1 + num_2


class Subtraction:
    """ This is the subtraction class"""

    @staticmethod
    def subtract(num_1, num_2):
        """ This is the sub method"""
        return num_1 - num_2


class Multiplication:
    """ This is the multiplication class"""

    @staticmethod
    def multiply(num_1, num_2):
        """ This is the mul method"""
        return num_1 * num_2


class Divison:
    """ This is the divison class"""

    @staticmethod
    def divide(num_1, num_2):
        """ This is the div method"""
        return num_1 / num_2